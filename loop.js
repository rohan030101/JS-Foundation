// //for loop is used to run the block code for set iteration.
console.log("For Loop");
for (a = 0; a <= 5; a++) {
    console.log(a);
}


// //while loop is used to run until the statement is valid given in the while loop 
console.log("While Loop");
var count = 0;
while(count<4){
    console.log(count);
    count++;
}

//do while is as similar as while loop but will run the code once before check the condtion of while

console.log("do while  Loop");
var i = 0;
do{
    console.log(i);
    i++
}while(i<=4);